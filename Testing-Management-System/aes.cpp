void AES::CFB_encrypt(const uint8_t* key, const uint8_t* iv, uint8_t* buf, size_t buf_size)
{
    round_key_t round_key;
    key_expansion(&round_key, (key_t*)key);
    uint8_t next_iv[Nstb];
    memcpy(next_iv, iv, Nstb);
    for (size_t i = 0; i < buf_size; i += Nstb, buf += Nstb)
    {
        encrypt((state_t*)next_iv, &round_key);
        xor_state_blocks(buf, next_iv);
        memcpy(next_iv, buf, Nstb);
    }
}

void AES::CFB_decrypt(const uint8_t* key, const uint8_t* iv, uint8_t* buf, size_t buf_size)
{
    round_key_t round_key;
    key_expansion(&round_key, (key_t*)key);
    uint8_t next_iv[Nstb];
    memcpy(next_iv, iv, Nstb);
    uint8_t temp_buf_state[Nstb];
    for (size_t i = 0; i < buf_size; i += Nstb, buf += Nstb)
    {
        memcpy(temp_buf_state, buf, Nstb);
        encrypt((state_t*)next_iv, &round_key);
        xor_state_blocks(buf, next_iv);
        memcpy(next_iv, temp_buf_state, Nstb);
    }
}


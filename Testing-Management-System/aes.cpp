void AES::key_expansion(round_key_t* round_key, const key_t* key)
{
    uint8_t i, j, prev_key_word_index, prev_round_key_word_index;
    uint8_t prev_key_word[Nwb];

    for (i = 0; i < Nk; ++i)
    {
        for (j = 0; j < Nwb; ++j)
        {
            (*round_key)[i][j] = (*key)[i][j];
        }
    }

    for (i = Nk; i < Nstw * (Nr + 1); ++i)
    {
        {
            prev_key_word_index = i - 1;
            for (j = 0; j < Nwb; ++j)
            {
                prev_key_word[j] = (*round_key)[prev_key_word_index][j];
            }
        }
        if (i % Nk == 0)
        {
            rot_word(prev_key_word);
            sub_word(prev_key_word);
            prev_key_word[0] = prev_key_word[0] ^ rcon[i / Nk];
        }
        {
            prev_round_key_word_index = i - Nk;
            for (j = 0; j < Nwb; ++j)
            {
                (*round_key)[i][j] = (*round_key)[prev_round_key_word_index][j] ^ prev_key_word[j];
            }
        }
    }
}

uint8_t AES::get_sbox_value(uint8_t num)
{
    return sbox[num];
}

void AES::rot_word(uint8_t* word) {
    const uint8_t word_0 = word[0];
    uint8_t i;
    for (i = 0; i < Nwb - 1; ++i)
    {
        word[i] = word[i + 1];
    }
    word[i] = word_0;
}

void AES::sub_word(uint8_t* word) {
    for (uint8_t i = 0; i < Nwb; ++i)
    {
        word[i] = get_sbox_value(word[i]);
    }
}

void AES::add_round_key(state_t* state, uint8_t round_index, const round_key_t* round_key)
{
    uint8_t i, j;
    for (i = 0; i < Nwb; ++i)
    {
        for (j = 0; j < Nstw; ++j)
        {
            (*state)[i][j] ^= (*round_key)[round_index * Nstw + i][j];
        }
    }
}

void AES::sub_bytes(state_t* state)
{
    uint8_t i, j;
    for (j = 0; j < Nstw; ++j)
    {
        for (i = 0; i < Nwb; ++i)
        {
            (*state)[i][j] = get_sbox_value((*state)[i][j]);
        }
    }
}

void AES::shift_rows(state_t* state)
{
    uint8_t temp_state_byte;
    temp_state_byte = (*state)[0][1];
    (*state)[0][1] = (*state)[1][1];
    (*state)[1][1] = (*state)[2][1];
    (*state)[2][1] = (*state)[3][1];
    (*state)[3][1] = temp_state_byte;

    temp_state_byte = (*state)[0][2];
    (*state)[0][2] = (*state)[2][2];
    (*state)[2][2] = temp_state_byte;

    temp_state_byte = (*state)[1][2];
    (*state)[1][2] = (*state)[3][2];
    (*state)[3][2] = temp_state_byte;

    temp_state_byte = (*state)[0][3];
    (*state)[0][3] = (*state)[3][3];
    (*state)[3][3] = (*state)[2][3];
    (*state)[2][3] = (*state)[1][3];
    (*state)[1][3] = temp_state_byte;
}

uint8_t AES::xtime(uint8_t x)
{
    return ((x << 1) ^ (((x >> 7) & 1) * 0x1b));
}

void AES::mix_columns(state_t* state)
{
    uint8_t i, xorStateBytes, prodStateBytes, state_0;
    for (i = 0; i < Nwb; ++i)
    {
        xorStateBytes = (*state)[i][0] ^ (*state)[i][1] ^ (*state)[i][2] ^ (*state)[i][3];
        state_0 = (*state)[i][0];
        prodStateBytes = (*state)[i][0] ^ (*state)[i][1]; prodStateBytes = xtime(prodStateBytes);  (*state)[i][0] ^= prodStateBytes ^ xorStateBytes;
        prodStateBytes = (*state)[i][1] ^ (*state)[i][2]; prodStateBytes = xtime(prodStateBytes);  (*state)[i][1] ^= prodStateBytes ^ xorStateBytes;
        prodStateBytes = (*state)[i][2] ^ (*state)[i][3]; prodStateBytes = xtime(prodStateBytes);  (*state)[i][2] ^= prodStateBytes ^ xorStateBytes;
        prodStateBytes = (*state)[i][3] ^ state_0;        prodStateBytes = xtime(prodStateBytes);  (*state)[i][3] ^= prodStateBytes ^ xorStateBytes;
    }
}

uint8_t AES::multiply(uint8_t x, uint8_t y)
{
    return (((y & 1) * x) ^ // 1x
        ((y >> 1 & 1) * xtime(x)) ^ // 2x
        ((y >> 2 & 1) * xtime(xtime(x))) ^ // 4x
        ((y >> 3 & 1) * xtime(xtime(xtime(x))))); // 8x
}

void AES::encrypt(state_t* state, const round_key_t* round_key)
{
    uint8_t round_index = 0;
    add_round_key(state, round_index, round_key);
    for (round_index = 1; ; ++round_index)
    {
        sub_bytes(state);
        shift_rows(state);
        if (round_index == Nr) break;
        mix_columns(state);
        add_round_key(state, round_index, round_key);
    }
    add_round_key(state, Nr, round_key);
}

